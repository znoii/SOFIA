BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "Companies" (
	"Id"	INTEGER NOT NULL,
	"Name"	TEXT NOT NULL,
	PRIMARY KEY("Id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "OrderProducts" (
	"OrderId"	INTEGER NOT NULL,
	"Article"	TEXT NOT NULL,
	"Count"	INTEGER NOT NULL,
	PRIMARY KEY("OrderId","Article"),
	FOREIGN KEY("Article") REFERENCES "Products"("Article"),
	FOREIGN KEY("OrderId") REFERENCES "PickUpPoint"("Id")
);
CREATE TABLE IF NOT EXISTS "OrderStatuses" (
	"Id"	INTEGER,
	"Name"	TEXT,
	PRIMARY KEY("Id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "Orders" (
	"OrderId"	INTEGER,
	"OrderedDate"	TEXT NOT NULL,
	"DeliveryDate"	TEXT NOT NULL,
	"PickUpPointId"	INTEGER NOT NULL,
	"UserId"	INTEGER NOT NULL,
	"DeliveryCode"	INTEGER NOT NULL,
	"StatusId"	INTEGER NOT NULL,
	PRIMARY KEY("OrderId" AUTOINCREMENT),
	FOREIGN KEY("StatusId") REFERENCES "OrderStatuses"("Id"),
	FOREIGN KEY("UserId") REFERENCES "Users"("Id")
);
CREATE TABLE IF NOT EXISTS "PickUpPoint" (
	"Id"	INTEGER,
	"Address"	TEXT NOT NULL,
	PRIMARY KEY("Id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "ProductCategories" (
	"Id"	INTEGER NOT NULL,
	"Name"	TEXT NOT NULL,
	PRIMARY KEY("Id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "Products" (
	"Article"	TEXT UNIQUE,
	"Name"	TEXT NOT NULL,
	"Unit"	TEXT NOT NULL,
	"Price"	INTEGER NOT NULL,
	"ProviderId"	INTEGER NOT NULL,
	"ManufacturerId"	INTEGER NOT NULL,
	"CategoryId"	INTEGER NOT NULL,
	"CurrentDiscount"	INTEGER NOT NULL,
	"Count"	INTEGER NOT NULL,
	"Description"	TEXT NOT NULL,
	"PhotoPath"	TEXT,
	PRIMARY KEY("Article"),
	FOREIGN KEY("CategoryId") REFERENCES "ProductCategories"("Id"),
	FOREIGN KEY("ManufacturerId") REFERENCES "Companies"("Id"),
	FOREIGN KEY("ProviderId") REFERENCES "Companies"("Id")
);
CREATE TABLE IF NOT EXISTS "Roles" (
	"Id"	INTEGER NOT NULL,
	"Name"	TEXT NOT NULL,
	PRIMARY KEY("Id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "Users" (
	"Id"	INTEGER NOT NULL,
	"RoleId"	INTEGER,
	"FullName"	TEXT NOT NULL,
	"Login"	TEXT NOT NULL UNIQUE,
	"Password"	TEXT NOT NULL,
	PRIMARY KEY("Id" AUTOINCREMENT)
);
